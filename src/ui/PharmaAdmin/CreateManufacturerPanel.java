    private void createDocBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createDocBtnActionPerformed
        // TODO add your handling code here:
        if(manufName.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Enter valid input!");
            return;
        }
        
        if (Pattern.matches("^[0-9]*$", manufName.getText())) {
            JOptionPane.showMessageDialog(this, "Name cannot contain numbers!");
            return;
        }
        
        if(manufUserName.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Enter hospital admin username!");
            return;
        }
        
        if(String.valueOf(manufPwd.getPassword()).trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Enter hospital admin passwords!");
            return;
        }
        
        int pwdLen = manufPwd.getPassword().length;
        if(pwdLen < 6) {
            JOptionPane.showMessageDialog(this, "Password should be minimum 6 characters!");
            return;
        }
        
        Connection dbConn = null;
        PreparedStatement manuStatement = null;
        PreparedStatement userStatement = null;

        Long userId = (long) (Math.random() * (9999 - 1) + 1);
        String manuQuery = "INSERT INTO project.manufacturer VALUES  (?, ?);";
        String userQuery = "INSERT INTO project.credentials VALUES (?, ?, ?, ?);";
        
        Manufacturer manu = new Manufacturer();
        manu.setManufacturerId(manufID);
        manu.setManufacturerName(manufName.getText());
        
        Credentials creds = new Credentials();
        creds.setUserId(userId);
        creds.setUsername(manufUserName.getText());
        creds.setPassword(String.valueOf(manufPwd.getPassword()));
        creds.setRole("Manufacturer Admin");

        try {
            dbConn = db.getConnection();
            if (dbConn != null) {
                dbConn.setAutoCommit(false);
                manuStatement = dbConn.prepareStatement(manuQuery);
                userStatement = dbConn.prepareStatement(userQuery);
                manuStatement.setLong(1, manu.getManufacturerId());
                manuStatement.setString(2, manu.getManufacturerName());

                userStatement.setLong(1, creds.getUserId());
                userStatement.setString(2, creds.getUsername());
                userStatement.setString(3, creds.getPassword());
                userStatement.setString(4, creds.getRole());
                if ((manuStatement.executeUpdate() > 0) && userStatement.executeUpdate() > 0) {
                    System.out.println("Created Manufacturer Successfully!");
                    dbConn.commit();
                } else {
                    System.out.println("Failed to create Manufacturer");
                    dbConn.rollback();
                }
            } else {
                System.out.println("DB connection not connected");
            }
        } catch (SQLException sqlExp) {
            //rollback the connection
            sqlExp.printStackTrace();
        } catch (Exception exp) {
            exp.printStackTrace();

        } finally {
            if (manuStatement != null) {
                try {
                    if (!manuStatement.isClosed()) {
                        manuStatement.close();
                    }
                } catch (SQLException err) {
                    err.printStackTrace();

                }
            }
            if (dbConn != null) {
                try {
                    if (!dbConn.isClosed()) {
                        db.closeConnection(dbConn);
                    }
                } catch (SQLException err) {
                    err.printStackTrace();

                }
            }
        }
    }//GEN-LAST:event_createDocBtnActionPerformed


