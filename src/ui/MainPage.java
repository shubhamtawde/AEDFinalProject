    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Connection dbConn = null;
        ResultSet dbResult = null;
        PreparedStatement sqlStatement = null;
        String sqlQuery = "SELECT * FROM project.credentials WHERE username = ? and password = ? LIMIT 1;";
        try {
            dbConn = db.getConnection();
            if (dbConn != null) {
                sqlStatement = dbConn.prepareStatement(sqlQuery);
                sqlStatement.setString(1, username.getText());
                sqlStatement.setString(2, String.valueOf(password.getPassword()));
                dbResult = sqlStatement.executeQuery();
                System.out.println(dbResult);
                if (!dbResult.next()) {
                    System.out.println("Result set is null!");
                } else {
                    User user = new User();
                    user.setUserId(dbResult.getLong(1));
                    user.setUsername(dbResult.getString(2));
                    user.setPassword(dbResult.getString(3));
                    user.setRole(dbResult.getString(4));
                    if(user.getRole().equals("Hospital Admin")) {
                        HospitalAdminHome hosp = new HospitalAdminHome();
                        hosp.setVisible(true);
                        setVisible(false);
                    }
                    else if(user.getRole().equals("Doctor")) {
                        DoctorHomePanel doc = new DoctorHomePanel();
                        doc.setVisible(true);
                        setVisible(false);
                    }
                    else if(user.getRole().equals("Admin")) {
                        AdminHomePanel admin = new AdminHomePanel();
                        admin.setVisible(true);
                        setVisible(false);
                    }
                }
            } else {
                System.out.println("DB connection not connected");
            }
        } catch (SQLException sqlExp) {
            //rollback the connection
            sqlExp.printStackTrace();
        } catch (Exception exp) {
            exp.printStackTrace();

        } finally {
            if (sqlStatement != null) {
                try {
                    if (!sqlStatement.isClosed()) {
                        sqlStatement.close();
                    }
                } catch (SQLException err) {
                    err.printStackTrace();

                }
            }
            if (dbConn != null) {
                try {
                    if (!dbConn.isClosed()) {
                        db.closeConnection(dbConn);
                    }
                } catch (SQLException err) {
                    err.printStackTrace();

                }
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

